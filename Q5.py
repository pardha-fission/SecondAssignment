class Mycalculator:

    def MyAdd(self,a,b):
        return a + b

    def MyDiff(self,a,b):
        return a - b

    def MyDiv(self,a,b):
        return a/b

    def MyMul(self,a,b):
        return a*b

    def MyPow(self,a,b):
        return a**b

